public class HelloWorld {
    private int i;
    private String s;

    public HelloWorld(String s) {
        this.s = s;
    }

    public HelloWorld() {
        i = 0;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getS() {
        return s;
    }

}